public class third_DataTypes {
    public static void main(String[] args) {
        // Primitive DT
        byte b = 8;
        System.out.println("DT -> byte b = "+b);
        char ch = 'a';
        System.out.println("DT -> char ch = "+ch);
        boolean bool = true;
        System.out.println("DT -> boolean bool = "+bool);
        float price = 10.5f;
        // double price = 10.5;
        System.out.println("DT -> float price = "+price);
        int number = 1;
        System.out.println("DT -> int number = "+number);
        // 'long' <- Successor of int
        // it stores the large integer value
        // 'double' <- Successor of float
        // it stores the large precision / decimal value
        short n = 2;
        System.out.println("DT -> short n = "+n);
    }
}
